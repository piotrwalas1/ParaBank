package przyk2;

public class Calculator {
    public int dod(int pierwszaLiczba, int drugaLiczba){
        return pierwszaLiczba + drugaLiczba;
    }
    public int odej(int firstnumber, int secondnumber){
        return firstnumber - secondnumber;
    }
    public int mnoz(int firstnumber, int secondnumber){
        return firstnumber * secondnumber;
    }
    public int dziel(int firstnumber, int secondnumber){
        return firstnumber / secondnumber;
    }
}
